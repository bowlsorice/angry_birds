def make_logs(info_list,base):
    logs = []
    for each in info_list:
        a_log = Log((each[0][0]+base,each[0][1]),
                each[1], each[2],each[3])
        logs.append(a_log)
    return logs

def make_hogs(info_list,base):
    hogs = []
    for info in info_list:
        a_hog = Hog((info[0][0] + base, info[0][1]), info[1])
        hogs.append(a_hog)
    return hogs

